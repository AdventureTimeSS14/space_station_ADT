using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;
using Content.Shared.Atmos;

namespace Content.Client.Supermatter;

[GenerateTypedNameReferences]
public sealed partial class SuppermatterControlWindow : DefaultWindow
{
	public SuppermatterControlWindow()
	{
    RobustXamlLoader.Load(this);
    IoCManager.InjectDependencies(this);
	}


	public void UpdatePercents(string procent)
	{
		ProcentsLabel.Text = $"Целостность: {procent}%";
	}

	public void UpdateGases(Dictionary<Gas, float> gases)
	{
		GasesLabel.Text = "";
		foreach (var gas in gases)
		{
			GasesLabel.Text = GasesLabel.Text + gas + "\n";
		}
	}
}