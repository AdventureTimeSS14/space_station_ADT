        private void HandleReagentToggle(ReagentId reagentId, Button button)
        {
            // Toggle the reagent selection
            var isCurrentlyToggled = button.Pressed;

            if (isCurrentlyToggled)
            {
                // Button was turned on - add to selected reagents
