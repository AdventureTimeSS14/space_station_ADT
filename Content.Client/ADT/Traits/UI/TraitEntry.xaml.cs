using Robust.Shared.Utility;
using static Robust.Shared.IoC.IoCManager;

namespace Content.Client.ADT.Traits.UI;

[GenerateTypedNameReferences]
public sealed partial class TraitEntry : PanelContainer
{
    [Dependency] private readonly IPrototypeManager _prototype = default!;
    [Dependency] private readonly ILocalizationManager _loc = default!;
    [Dependency] private readonly IPlayerManager _playerManager = default!;
    [Dependency] private readonly JobRequirementsManager _requirementsManager = default!;

    public event Action<bool>? OnToggled;

    public bool IsSelected => TraitCheckbox.Pressed;
    public readonly int TraitCost;
    public bool MeetsConditions { get; private set; } = true;

    private readonly TraitPrototype _trait;
    private bool _isUpdating;
    private FormattedMessage requirements = new();

    public TraitEntry(TraitPrototype trait)
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        _trait = trait;
        TraitCost = trait.Cost;

        // Enable mouse events so tooltips work
        MouseFilter = MouseFilterMode.Pass;

        TraitNameLabel.Text = Loc.GetString(trait.Name);
        TraitDescriptionLabel.SetMessage(Loc.GetString(trait.Description ?? string.Empty));

        // Format cost display
        var costPrefix = trait.Cost > 0 ? "+" : "";
        var costColor = trait.Cost > 0 ? "#ff6b6b" : trait.Cost < 0 ? "#6bff6b" : "#888888";
        TraitCostLabel.Text = $"{costPrefix}{trait.Cost}";
        TraitCostLabel.ModulateSelfOverride = Color.FromHex(costColor);

        TraitCheckbox.OnToggled += OnCheckboxToggled;
    }

    /// <summary>
    /// Updates whether conditions are met based on current job/species.
    /// </summary>
    public void UpdateConditionsMet(HumanoidCharacterProfile? profile, ProtoId<JobPrototype>? jobId = null)
    {
        MeetsConditions = true;

        // Check species whitelist/blacklist
        if (profile != null)
        {
            if (_trait.SpeciesWhitelist.Count > 0 && !_trait.SpeciesWhitelist.Contains(profile.Species))
            {
                MeetsConditions = false;
                requirements = FormattedMessage.FromUnformatted(Loc.GetString("trait-species-unavailable"));
            }
            else if (_trait.SpeciesBlacklist.Contains(profile.Species))
            {
                MeetsConditions = false;
                requirements = FormattedMessage.FromUnformatted(Loc.GetString("trait-species-unavailable"));
            }
        }

        // Check job/department whitelist/blacklist
        // If jobId is null (character editor), check against profile's high-priority jobs
        if (MeetsConditions && profile != null)
        {
            var jobsToCheck = new List<ProtoId<JobPrototype>>();
            
            // If jobId is provided, use it; otherwise check high-priority jobs from profile
            if (jobId.HasValue)
            {
                jobsToCheck.Add(jobId.Value);
            }
            else
            {
                // Check jobs with High or Medium priority
                foreach (var (profileJobId, priority) in profile.JobPriorities)
                {
                    if (priority == JobPriority.High || priority == JobPriority.Medium)
                        jobsToCheck.Add(profileJobId);
                }
            }

            if (jobsToCheck.Count > 0)
            {
                var isBlocked = CheckJobRestrictions(jobsToCheck);
                if (isBlocked)
                {
                    MeetsConditions = false;
                    requirements = FormattedMessage.FromUnformatted(Loc.GetString("trait-job-unavailable"));
                }
            }
        }

        if (MeetsConditions && !_requirementsManager.CheckRequirementsForNonRole(_trait.Requirements, _playerManager.LocalSession, profile, out var reason))
        {
            MeetsConditions = false;
            if (reason is not null)
                requirements = reason;
        }

        UpdateDisabledState();
    }

    /// <summary>
    /// Checks if any of the provided jobs are blocked by job/department restrictions.
    /// </summary>
    private bool CheckJobRestrictions(List<ProtoId<JobPrototype>> jobsToCheck)
    {
        foreach (var checkJobId in jobsToCheck)
        {
            // Check job blacklist
            if (_trait.JobBlacklist.Contains(checkJobId))
                return true;

            // Check job whitelist
            if (_trait.JobWhitelist.Count > 0 && !_trait.JobWhitelist.Contains(checkJobId))
                return true;

            // Check department restrictions
            if (_trait.DepartmentWhitelist.Count > 0 || _trait.DepartmentBlacklist.Count > 0)
            {
                var allDepartments = new List<ProtoId<DepartmentPrototype>>();
                
                // Get all departments for this job
                foreach (var deptProto in _prototype.EnumeratePrototypes<DepartmentPrototype>())
                {
                    if (deptProto.Roles.Contains(checkJobId))
                        allDepartments.Add(deptProto.ID);
                }

                // Check department whitelist
                if (_trait.DepartmentWhitelist.Count > 0)
                {
                    var hasWhitelistedDept = false;
                    foreach (var deptId in allDepartments)
                    {
                        if (_trait.DepartmentWhitelist.Contains(deptId))
                        {
                            hasWhitelistedDept = true;
                            break;
                        }
                    }

                    if (!hasWhitelistedDept)
                        return true;
                }

                // Check department blacklist
                if (_trait.DepartmentBlacklist.Count > 0)
                {
                    foreach (var deptId in allDepartments)
                    {
                        if (_trait.DepartmentBlacklist.Contains(deptId))
                            return true;
                    }
                }
            }
        }

        return false;
    }

    private void UpdateDisabledState()
    {
        if (!MeetsConditions)
        {
            // Hide checkbox, show lock icon
            TraitCheckbox.Visible = false;
            LockIcon.Visible = true;

            // Deselect if conditions no longer met
            if (TraitCheckbox.Pressed)
            {
                _isUpdating = true;
                TraitCheckbox.Pressed = false;
                UpdateSelectedStyle();
                _isUpdating = false;
                OnToggled?.Invoke(false);
            }

            // Add disabled styling
            AddStyleClass("TraitsEntryDisabled");

            // Update tooltip to show failed requirements
            var tooltip = new Tooltip();
            tooltip.SetMessage(requirements);
            TraitPanel.TooltipSupplier = _ => tooltip;
        }
        else
        {
            // Show checkbox, hide lock icon
            TraitCheckbox.Visible = true;
            LockIcon.Visible = false;

            // Remove disabled styling - stylesheet restores normal colors
            RemoveStyleClass("TraitsEntryDisabled");
        }
    }

    private void OnCheckboxToggled(BaseButton.ButtonToggledEventArgs args)
    {
        if (_isUpdating)
            return;

        if (!MeetsConditions)
        {
            // This shouldn't happen since checkbox is hidden, but just in case
            _isUpdating = true;
            TraitCheckbox.Pressed = false;
            _isUpdating = false;
            return;
        }

        UpdateSelectedStyle();
        OnToggled?.Invoke(args.Pressed);
    }

    public void SetSelected(bool selected)
    {
        _isUpdating = true;
        TraitCheckbox.Pressed = selected && MeetsConditions;
        UpdateSelectedStyle();
        _isUpdating = false;
    }

    private void UpdateSelectedStyle()
    {
        if (TraitCheckbox.Pressed)
            AddStyleClass("TraitsEntrySelected");
        else
            RemoveStyleClass("TraitsEntrySelected");
    }
}
