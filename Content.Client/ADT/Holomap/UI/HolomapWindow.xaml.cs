using Content.Shared.ADT.Holomap;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.ADT.Holomap.UI;

[GenerateTypedNameReferences]
public sealed partial class HolomapWindow : BaseWindow
{
    public event Action<HolomapMode>? OnModeSelected;

    public HolomapWindow()
    {
        RobustXamlLoader.Load(this);

        BattlemapButton.OnPressed += _ => OnModeSelected?.Invoke(HolomapMode.Battlemap);
        LavalandButton.OnPressed += _ => OnModeSelected?.Invoke(HolomapMode.Lavaland);
    }
}
